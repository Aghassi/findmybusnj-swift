import UIKit

public class ColorPalette: UIColor {
  public func powderBlue() -> UIColor {
    return  UIColor(red: 67.0/255.0, green: 174.0/255.0, blue: 249.0/255.0, alpha: 1.0)
  }
  
  public func emeraldGreen() -> UIColor {
    return UIColor(red: 29.0/255.0, green: 156.0/255.0, blue: 48.0/255.0, alpha: 1.0)
  }
  
  public func creamsicleOrange() -> UIColor {
    return UIColor(red: 237.0/255.0, green: 145.0/255.0, blue: 50.0/255.0, alpha: 1.0)
  }
  
  public func lollipopRed() -> UIColor {
    return UIColor(red: 204.0/255.0, green: 25.0/255.0, blue: 36.0/255.0, alpha: 1.0)
  }
}
